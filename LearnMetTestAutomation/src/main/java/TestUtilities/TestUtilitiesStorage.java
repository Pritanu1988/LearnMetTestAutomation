package TestUtilities;

public class TestUtilitiesStorage {
	
	public String TestStepNo;
	public String TestDetails;
	public String WebElementLocator;
	public String TypeofLocator;
	public String Method;
	public String Parameter;
	public String Delay;
	
	public String getTestStepNo() {
		return TestStepNo;
	}
	public String setTestStepNo(String testStepNo) {
		return TestStepNo = testStepNo;
	}
	public String getTestDetails() {
		return TestDetails;
	}
	public String setTestDetails(String testDetails) {
		return TestDetails = testDetails;
	}
	public String getWebElementLocator() {
		return WebElementLocator;
	}
	public String setWebElementLocator(String webElementLocator) {
		return WebElementLocator = webElementLocator;
	}
	public String getTypeofLocator() {
		return TypeofLocator;
	}
	public String setTypeofLocator(String typeofLocator) {
		return TypeofLocator = typeofLocator;
	}
	public String getMethod() {
		return Method;
	}
	public String setMethod(String method) {
		return Method = method;
	}
	public String getParameter() {
		return Parameter;
	}
	public String setParameter(String parameter) {
		return Parameter = parameter;
	}
	public String getDelay() {
		return Delay;
	}
	public String setDelay(String delay) {
		return Delay = delay;
	}

}
